"""Idempotent local schema repair for the asset tax engine.

This repair script is intended for local/dev databases whose Alembic history
drifted but already contain most post-040 application columns. It safely adds
the asset tax engine foundation from revisions 049 and 050 without relying on
the broken migration graph.
"""

from __future__ import annotations

import os
from pathlib import Path

from dotenv import load_dotenv
from sqlalchemy import create_engine, text


def _dsn() -> str:
    load_dotenv(Path(__file__).resolve().parents[1] / ".env")
    return (
        f"postgresql://{os.environ['POSTGRES_USER']}:{os.environ['POSTGRES_PASSWORD']}"
        f"@{os.environ['POSTGRES_SERVER']}:{os.environ.get('POSTGRES_PORT', '5432')}"
        f"/{os.environ['POSTGRES_DB']}"
    )


STATEMENTS = [
    """
    DO $$
    BEGIN
        IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'asseteventtype') THEN
            CREATE TYPE asseteventtype AS ENUM (
                'acquired',
                'put_into_use',
                'reclassified',
                'business_use_changed',
                'degressive_to_linear_switch',
                'ifb_flagged',
                'ifb_claimed',
                'sold',
                'scrapped',
                'private_withdrawal'
            );
        END IF;
    END $$;
    """,
    """
    DO $$
    BEGIN
        IF NOT EXISTS (SELECT 1 FROM pg_type WHERE typname = 'asseteventtriggersource') THEN
            CREATE TYPE asseteventtriggersource AS ENUM (
                'system',
                'user',
                'policy_recompute',
                'import'
            );
        END IF;
    END $$;
    """,
    """
    CREATE TABLE IF NOT EXISTS asset_policy_snapshots (
        id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        user_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
        property_id UUID NOT NULL REFERENCES properties(id) ON DELETE CASCADE,
        policy_version VARCHAR(50) NOT NULL DEFAULT 'asset_tax_engine_v1',
        jurisdiction VARCHAR(10) NOT NULL DEFAULT 'AT',
        effective_anchor_date DATE NOT NULL,
        snapshot_payload JSON NOT NULL,
        rule_ids JSON NULL,
        created_at TIMESTAMP NOT NULL DEFAULT now()
    );
    """,
    "CREATE INDEX IF NOT EXISTS ix_asset_policy_snapshots_id ON asset_policy_snapshots (id);",
    "CREATE INDEX IF NOT EXISTS ix_asset_policy_snapshots_user_id ON asset_policy_snapshots (user_id);",
    "CREATE INDEX IF NOT EXISTS ix_asset_policy_snapshots_property_id ON asset_policy_snapshots (property_id);",
    "CREATE INDEX IF NOT EXISTS ix_asset_policy_snapshots_effective_anchor_date ON asset_policy_snapshots (effective_anchor_date);",
    """
    CREATE TABLE IF NOT EXISTS asset_events (
        id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
        user_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
        property_id UUID NOT NULL REFERENCES properties(id) ON DELETE CASCADE,
        event_type asseteventtype NOT NULL,
        trigger_source asseteventtriggersource NOT NULL DEFAULT 'system',
        event_date DATE NOT NULL,
        payload JSON NULL,
        created_at TIMESTAMP NOT NULL DEFAULT now()
    );
    """,
    "CREATE INDEX IF NOT EXISTS ix_asset_events_id ON asset_events (id);",
    "CREATE INDEX IF NOT EXISTS ix_asset_events_user_id ON asset_events (user_id);",
    "CREATE INDEX IF NOT EXISTS ix_asset_events_property_id ON asset_events (property_id);",
    "CREATE INDEX IF NOT EXISTS ix_asset_events_event_type ON asset_events (event_type);",
    "CREATE INDEX IF NOT EXISTS ix_asset_events_event_date ON asset_events (event_date);",
    "ALTER TABLE properties ADD COLUMN IF NOT EXISTS acquisition_kind VARCHAR(30);",
    "ALTER TABLE properties ADD COLUMN IF NOT EXISTS put_into_use_date DATE;",
    "ALTER TABLE properties ADD COLUMN IF NOT EXISTS is_used_asset BOOLEAN NOT NULL DEFAULT FALSE;",
    "ALTER TABLE properties ADD COLUMN IF NOT EXISTS first_registration_date DATE;",
    "ALTER TABLE properties ADD COLUMN IF NOT EXISTS prior_owner_usage_years NUMERIC(5, 2);",
    "ALTER TABLE properties ADD COLUMN IF NOT EXISTS comparison_basis VARCHAR(10);",
    "ALTER TABLE properties ADD COLUMN IF NOT EXISTS comparison_amount NUMERIC(12, 2);",
    "ALTER TABLE properties ADD COLUMN IF NOT EXISTS gwg_eligible BOOLEAN NOT NULL DEFAULT FALSE;",
    "ALTER TABLE properties ADD COLUMN IF NOT EXISTS gwg_elected BOOLEAN NOT NULL DEFAULT FALSE;",
    "ALTER TABLE properties ADD COLUMN IF NOT EXISTS depreciation_method VARCHAR(20) DEFAULT 'linear';",
    "ALTER TABLE properties ADD COLUMN IF NOT EXISTS degressive_afa_rate NUMERIC(5, 4);",
    "ALTER TABLE properties ADD COLUMN IF NOT EXISTS useful_life_source VARCHAR(50);",
    "ALTER TABLE properties ADD COLUMN IF NOT EXISTS income_tax_cost_cap NUMERIC(12, 2);",
    "ALTER TABLE properties ADD COLUMN IF NOT EXISTS income_tax_depreciable_base NUMERIC(12, 2);",
    "ALTER TABLE properties ADD COLUMN IF NOT EXISTS vat_recoverable_status VARCHAR(20);",
    "ALTER TABLE properties ADD COLUMN IF NOT EXISTS ifb_candidate BOOLEAN NOT NULL DEFAULT FALSE;",
    "ALTER TABLE properties ADD COLUMN IF NOT EXISTS ifb_rate NUMERIC(5, 4);",
    "ALTER TABLE properties ADD COLUMN IF NOT EXISTS ifb_rate_source VARCHAR(50);",
    "ALTER TABLE properties ADD COLUMN IF NOT EXISTS recognition_decision VARCHAR(50);",
    "ALTER TABLE properties ADD COLUMN IF NOT EXISTS policy_confidence NUMERIC(5, 4);",
]


def main() -> None:
    engine = create_engine(_dsn())
    with engine.begin() as conn:
        for index, statement in enumerate(STATEMENTS, start=1):
            conn.execute(text(statement))
            print(f"[{index}/{len(STATEMENTS)}] applied")
    print("Asset tax schema repair completed.")


if __name__ == "__main__":
    main()
